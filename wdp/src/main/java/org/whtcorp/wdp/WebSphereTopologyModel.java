package org.whtcorp.wdp;

public class WebSphereTopologyModel {
	
	private String websphere_cell_name;
	private String websphere_cluster_name;
	private String websphere_node_name;
	private String websphere_server_name;
	
	public String getWebsphere_cell_name() {
		return websphere_cell_name;
	}
	
	public void setWebsphere_cell_name(String websphere_cell_name) {
		this.websphere_cell_name = websphere_cell_name;
	}
	
	public String getWebsphere_cluster_name() {
		return websphere_cluster_name;
	}
	
	public void setWebsphere_cluster_name(String websphere_cluster_name) {
		this.websphere_cluster_name = websphere_cluster_name;
	}
	
	public String getWebsphere_node_name() {
		return websphere_node_name;
	}
	
	public void setWebsphere_node_name(String websphere_node_name) {
		this.websphere_node_name = websphere_node_name;
	}
	
	public String getWebsphere_server_name() {
		return websphere_server_name;
	}
	
	public void setWebsphere_server_name(String websphere_server_name) {
		this.websphere_server_name = websphere_server_name;
	}
}
