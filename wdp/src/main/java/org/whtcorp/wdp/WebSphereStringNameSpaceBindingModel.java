package org.whtcorp.wdp;

public class WebSphereStringNameSpaceBindingModel {
    
    private String name;
    private String nameInNameSpace;
    private String stringToBind;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getNameInNameSpace() {
        return nameInNameSpace;
    }

    public void setNameInNameSpace(String nameInNameSpace) {
        this.nameInNameSpace = nameInNameSpace;
    }

    public String getStringToBind() {
        return stringToBind;
    }

    public void setStringToBind(String stringToBind) {
        this.stringToBind = stringToBind;
    }
}
